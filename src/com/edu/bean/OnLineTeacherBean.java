package com.edu.bean;

/**
 * 折线图的实体类
 * 
 * @author Administrator
 *
 */
public class OnLineTeacherBean {

	private int code;
	private String msg;
	private OnlineBean results;

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public OnlineBean getResults() {
		return results;
	}

	public void setResults(OnlineBean results) {
		this.results = results;
	}

}
