package com.edu.bean;

/**
 * 折线图的实体类
 * 
 * @author Administrator
 *
 */
public class AllBrokenLine {

	private int code;
	private String msg;
	private BrokenLine result;

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public BrokenLine getResult() {
		return result;
	}

	public void setResult(BrokenLine result) {
		this.result = result;
	}

}
