package com.edu.bean;

public class RadarBean {
	private int code;
	private String msg;
	private RadarResultBean result;
	
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public RadarResultBean getResult() {
		return result;
	}
	public void setResult(RadarResultBean result) {
		this.result = result;
	}
}
