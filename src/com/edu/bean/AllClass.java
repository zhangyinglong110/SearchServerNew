package com.edu.bean;

import java.util.List;

public class AllClass {
	private int code;
	private String msg;
	private List<ClassBean> results;

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public List<ClassBean> getResults() {
		return results;
	}

	public void setResults(List<ClassBean> results) {
		this.results = results;
	}

}
